package pages;

public class CalculatorPage {

    public int sum(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }


    public int div(int a, int b) {
        return a / b;
    }


    public double multiply(double a, double b) {
        return a * b;
    }
}


